package org.examples.web;

public class Model {
    String sName;
    int nAge;
    String sNotes;

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public int getnAge() {
        return nAge;
    }

    public void setnAge(int nAge) {
        this.nAge = nAge;
    }

    public String getsNotes() {
        return sNotes;
    }

    public void setsNotes(String sNotes) {
        this.sNotes = sNotes;
    }
}
